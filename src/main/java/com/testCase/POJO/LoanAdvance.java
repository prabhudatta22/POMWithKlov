public class LoanAdvance {

	
	private String installmentNo;	
	private String installment_Month;	
	private String opening_Balance;	
	private String repayment;	
	private String instalment_Amount;	
	private String principal_Balance;
	
	
	/**
	 * @return the installmentNo
	 */
	public String getInstallmentNo() {
		return installmentNo;
	}
	/**
	 * @param installmentNo the installmentNo to set
	 */
	public void setInstallmentNo(String installmentNo) {
		this.installmentNo = installmentNo;
	}
	/**
	 * @return the installment_Month
	 */
	public String getInstallment_Month() {
		return installment_Month;
	}
	/**
	 * @param installment_Month the installment_Month to set
	 */
	public void setInstallment_Month(String installment_Month) {
		this.installment_Month = installment_Month;
	}
	/**
	 * @return the opening_Balance
	 */
	public String getOpening_Balance() {
		return opening_Balance;
	}
	/**
	 * @param opening_Balance the opening_Balance to set
	 */
	public void setOpening_Balance(String opening_Balance) {
		this.opening_Balance = opening_Balance;
	}
	/**
	 * @return the repayment
	 */
	public String getRepayment() {
		return repayment;
	}
	/**
	 * @param repayment the repayment to set
	 */
	public void setRepayment(String repayment) {
		this.repayment = repayment;
	}
	/**
	 * @return the instalment_Amount
	 */
	public String getInstalment_Amount() {
		return instalment_Amount;
	}
	/**
	 * @param instalment_Amount the instalment_Amount to set
	 */
	public void setInstalment_Amount(String instalment_Amount) {
		this.instalment_Amount = instalment_Amount;
	}
	/**
	 * @return the principal_Balance
	 */
	public String getPrincipal_Balance() {
		return principal_Balance;
	}
	/**
	 * @param principal_Balance the principal_Balance to set
	 */
	public void setPrincipal_Balance(String principal_Balance) {
		this.principal_Balance = principal_Balance;
	}
	
	
}
